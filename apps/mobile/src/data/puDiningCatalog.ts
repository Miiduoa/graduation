function buildCafeteriaId(schoolId: string, slug: string) {
  return `${schoolId}-caf-${slug}`;
}

function buildMenuId(schoolId: string, slug: string) {
  return `${schoolId}-menu-${slug}`;
}

export function isProvidenceDiningSchoolId(schoolId?: string | null): boolean {
  return schoolId === 'pu' || schoolId === 'tw-pu';
}

export function getPuDiningCafeterias(schoolId: string): Cafeteria[] {
  return PU_CAFETERIAS.map((cafeteria) => ({
    ...cafeteria,
    id: buildCafeteriaId(schoolId, cafeteria.slug),
    schoolId,
  }));
}

export function getPuDiningMenuItems(schoolId: string): MenuItem[] {
  const cafeterias = new Map(
    PU_CAFETERIAS.map((cafeteria) => [
      cafeteria.slug,
      {
        ...cafeteria,
        id: buildCafeteriaId(schoolId, cafeteria.slug),
        schoolId,
      },
    ]),
  );

  return PU_MENU_ITEMS.map((item) => {
    const cafeteria = cafeterias.get(item.cafeteriaSlug);
    if (!cafeteria) {
      throw new Error(`Unknown PU cafeteria slug: ${item.cafeteriaSlug}`);
    }

    return {
      ...item,
      id: buildMenuId(schoolId, item.slug),
      schoolId,
      cafeteriaId: cafeteria.id,
      cafeteria: cafeteria.name,
    };
  });
}

export function hasPuOfficialCafeteriaName(name?: string | null): boolean {
  return PU_OFFICIAL_CAFETERIA_NAMES.has(String(name ?? '').trim());
}

export function hasPuOfficialMenuSignal(input: {
  name?: string | null;
  cafeteria?: string | null;
}): boolean {
  const haystack = `${String(input.name ?? '')} ${String(input.cafeteria ?? '')}`;
  return PU_OFFICIAL_MENU_TOKENS.some((token) => haystack.includes(token));
}

